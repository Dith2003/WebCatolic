                <h3>Interior del templo</h3>
                <p>Dentro del templo se aprecian nervaduras de cantera, de estilo gótico en el techo del sotocoro, en medio de las cuales se ven hermosas pinturas policromadas de estilo renacentista. En las paredes de este vestíbulo hay pinturas murales muy deterioradas, pero todavía se puede apreciar el águila con las águilas extendidas, parada sobre lo que puede ser un nopal, tiene un penacho de plumas policromado, de su pico surge una lengua en forma de flecha y dos vírgulas (símbolo prehispánico de la palabra). La pintura del lado sur esta mejor conservada, se aprecian el águila con dos jaguares, con uno de los jaguares parece discutir o platicar. En la bóveda de laceria anterior al presbiterio está pintada otra águila donde esta reunido un gran número de elementos prehispánicos como son el banderín o pantli, pamitl o panitl, el tlauhquechol o penacho, el cerro o teptl, el nopal, las vírgulas, el chimali o rodela que cuelga del cuello del ave, el símbolo del agua atl, todo lo anterior compensado con la figura europea de la hoja de acanto.</p>
                <br>
                <p>En el lado derecho, desde la entrada hasta el altar mayor se han conservado las pinturas murales descubiertas por el cura del lugar, Don Alfonso Moreno en 1959. Representan un curioso contraste temático donde se aprecian personajes indígenas y temas renacentistas en una mezcla de color y figura. Con las imágenes plasmadas en los muros, los frailes se auxiliaban para predicar la religión. Las pinturas que se conservan en el templo de Ixmiquilpan no contienen motivos cristianos, curiosamente abundan los motivos indios entremezclándose con hojas de acanto y personajes ocupados en batallas, luciendo vestidos, insignias y armas indígenas.</p>
                <br>
                <p>El significado de estas pinturas no ha sido descifrado realmente puesto que hay varias interpretaciones, ¿Por qué pelan indígenas con sus armas y vistiendo de guerreros tigres y coyotes? ¿Qué representan los seres fantásticos que luchan con los indios? La interpretación de las pinturas murales del templo es diversa, se piensa que es una autentica batalla en la que participan indígenas, centauros y animales fantásticos, un guerrero sostiene un escudo en cuyo centro hay un rostro de mujer. Otra interpretación, no fundamentada, sostiene que se trata de una alegoría sobre la defensa de la religión cristiana, hecha por los indios creyentes, en contra de otros indios enemigos de la nueva fe. Existen diferentes explicaciones, pero la realidad es que se encuentran pintados elementos indígenas prehispánicos impropios de un lugar donde estos motivos no tenían cabina. En el año de 1622 se levanto una capilla anexa al templo mayor dedicada al señor de Mapethé.</p>
